#include<iostream>
using namespace std;
#include<string>
void main6() {
	// 整型

	int a = 0;
	cout << "输入整型" << endl;
	cin >> a;
	cout << "输入的是" << a << endl;

	// 浮点型
	float f = 0;
	cout << "输入浮点型" << endl;
	cin >> f;
	cout << "输入的是" << f << endl;

	// 字符型
	char ch = 'a';
	cout << "输入字符型" << endl;
	cin >> ch;
	cout << "输入的是" << ch << endl;

	// 字符串
	string str = "";
	cout << "输入字符串" << endl;
	cin >> str;
	cout << "输入的是" << str << endl;

	//布尔型
	bool flag = false;
	cout << "输入布尔型" << endl;
	cin >> flag;
	cout << "输入的是" << flag << endl;


	system("pause");
}